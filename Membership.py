#Membership operators

# Define two lists
list1 = [1, 2, 3]
list2 = [4, 5, 6]

# Membership operator - in
if 1 in list1:
    print("1 is present in list1")
else:
    print("1 is not present in list1")

# Membership operator - not in
if 1 not in list1:
    print("1 is not present in list1")
else:
    print(list2)

